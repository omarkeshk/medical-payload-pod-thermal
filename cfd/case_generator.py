def write_fvSolution(system_dir: Path, tight: bool = False) -> None:
    """Write fvSolution.

    tight=False  → fan-on default (fast convergence on forced convection)
    tight=True   → fan-off / buoyancy-dominated case: tighter relaxation,
                   stricter p_rgh tolerance, longer-step h relaxation.
                   Required for natural-convection SIMPLE convergence.
    """
    if tight:
        U_relax       = 0.3
        scalar_relax  = 0.3
        field_relax   = 0.3
        p_rgh_resid   = "5e-4"
    else:
        U_relax       = 0.7
        scalar_relax  = 0.5
        field_relax   = 0.7
        p_rgh_resid   = "1e-3"

    content = _foam_header("dictionary", "fvSolution") + dedent(f"""\
        solvers
        {{
            p_rgh
            {{
                solver          GAMG;
                tolerance       1e-7;
                relTol          0.01;
                smoother        GaussSeidel;
            }}
            "(U|h|k|omega|epsilon)"
            {{
                solver          PBiCGStab;
                preconditioner  DILU;
                tolerance       1e-7;
                relTol          0.1;
            }}
        }}
        SIMPLE
        {{
            momentumPredictor   yes;
            nNonOrthogonalCorrectors 0;
            consistent          yes;
            pRefCell            0;
            pRefValue           100000;
            residualControl
            {{
                p_rgh           {p_rgh_resid};
                U               1e-4;
                h               1e-4;
                "(k|omega|epsilon)" 1e-3;
            }}
        }}
        relaxationFactors
        {{
            equations
            {{
                U               {U_relax};
                "(p_rgh|h|k|omega|epsilon)" {scalar_relax};
            }}
            fields
            {{
                p_rgh           {field_relax};
                rho             1.0;
            }}
        }}
        """)
    (system_dir / "fvSolution").write_text(content)


