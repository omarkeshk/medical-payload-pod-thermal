def generate_case(params: dict, target_dir: str | Path) -> Path:
    """Generate a complete OpenFOAM buoyantSimpleFoam case from params.

    Parameters
    ----------
    params : dict with keys
        wall_thickness_mm  : float
        wall_k             : float W/mK (PLA=0.2, aluminum=200)
        ambient_C          : float (e.g. 25.0)
        heat_total_W       : float (e.g. 17.0)
        mesh_nx, ny, nz    : int
        end_iter           : int (optional, default 2000)
        write_interval     : int (optional, default 500)
        h_ext              : float W/m2K external convection (optional)

    Returns
    -------
    Path : the case directory.
    """
    target = Path(target_dir).expanduser().resolve()
    target.mkdir(parents=True, exist_ok=True)

    true_vents = bool(params.get("true_vents"))

    # Fields go into 0.orig; the runner copies 0.orig -> 0 after the mesh
    # utilities (blockMesh / topoSet / createPatch) so the field boundary
    # entries always match the final patch list.
    zero_dir = target / "0.orig"
    constant_dir = target / "constant"
    system_dir = target / "system"
    for d in (zero_dir, constant_dir, system_dir):
        d.mkdir(exist_ok=True)

    # mesh + solver dicts
    write_blockMeshDict(params, system_dir)
    write_topoSetDict(params, system_dir)
    if true_vents:
        write_createPatchDict(params, system_dir)
    write_controlDict(params, system_dir)
    write_fvSchemes(system_dir)
    write_fvSolution(system_dir, tight=params.get("tight_relaxation", False))
    write_decomposeParDict(params, system_dir)

    # constant
    write_thermophysical(constant_dir)
    write_turbulence_properties(constant_dir)
    write_g(constant_dir)
    write_fvOptions(params, constant_dir)

    # initial fields
    if true_vents:
        write_T_tv(params, zero_dir)
        write_U_tv(params, zero_dir)
        write_p_tv(params, zero_dir)
        write_p_rgh_tv(params, zero_dir)
        write_turbulence_fields_tv(params, zero_dir)
    else:
        write_T(params, zero_dir)
        write_U(params, zero_dir)
        write_p(params, zero_dir)
        write_p_rgh(params, zero_dir)
        write_turbulence_fields(params, zero_dir)

    return target


