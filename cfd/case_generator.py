def write_turbulence_fields(p, zero_dir: Path) -> None:
    """k, omega, nut, alphat, epsilon for inlet/outlet/wall patches.

    NOTE: inlet/outlet are patch-type, not wall-type, so they cannot
    use wall functions. For sealed mode we use zeroGradient on them
    (they're physically closed but we keep them as patch type for
    BC flexibility when the fan is on).
    """
    fan_on = p.get("fan_rpm", 0) > 1.0
    walls = ("floor", "ceiling", "side_x0", "side_x1")
    inout = ("inlet", "outlet")  # always patch type

    def block(wall_bc: str, inlet_bc: str, outlet_bc: str) -> str:
        lines = []
        for w in walls:
            lines.append(f"            {w} {{ {wall_bc} }}")
        for io in inout:
            bc = inlet_bc if io == "inlet" else outlet_bc
            lines.append(f"            {io} {{ {bc} }}")
        return "\n".join(lines)

    # Wall treatment chosen for the y+ regime of this mesh.
    # Fine mesh (110×80×60 over 90×65×50 mm) → first-cell y+ ≈ 2, which is the
    # viscous-sublayer / buffer-layer range. Low-Re wall functions are the
    # correct choice; standard wall functions (kqRWallFunction, nutkWallFunction)
    # require y+ ≥ 30 and would mis-predict near-wall turbulence here.
    fields = {
        "k": (
            "[0 2 -2 0 0 0 0]", "0.001",
            block("type kLowReWallFunction; value uniform 1e-10;",
                  "type fixedValue; value uniform 0.001;",
                  "type inletOutlet; inletValue uniform 0.001; value uniform 0.001;"),
        ),
        "omega": (
            "[0 0 -1 0 0 0 0]", "1.0",
            # omegaWallFunction is Menter-blended — valid across the full y+
            # range from viscous sublayer to log-law region.
            block("type omegaWallFunction; value uniform 1.0;",
                  "type fixedValue; value uniform 1.0;",
                  "type inletOutlet; inletValue uniform 1.0; value uniform 1.0;"),
        ),
        "epsilon": (
            "[0 2 -3 0 0 0 0]", "0.01",
            block("type epsilonWallFunction; value uniform 0.01;",
                  "type fixedValue; value uniform 0.01;",
                  "type inletOutlet; inletValue uniform 0.01; value uniform 0.01;"),
        ),
        "nut": (
            "[0 2 -1 0 0 0 0]", "0",
            block("type nutLowReWallFunction; value uniform 0;",
                  "type calculated; value uniform 0;",
                  "type calculated; value uniform 0;"),
        ),
        "alphat": (
            "[1 -1 -1 0 0 0 0]", "0",
            block("type compressible::alphatWallFunction; Prt 0.85; value uniform 0;",
                  "type calculated; value uniform 0;",
                  "type calculated; value uniform 0;"),
        ),
    }

    for name, (dims, init, body) in fields.items():
        content = _foam_header("volScalarField", name) + dedent(f"""\
            dimensions      {dims};
            internalField   uniform {init};
            boundaryField
            {{
{body}
            }}
            """)
        (zero_dir / name).write_text(content)


# ---------------------------------------------------------------------------
# true_vents field writers — patches:
#   walls : floor, ceiling, side_x0, side_x1, side_y0, side_y1
#   open  : fan (x=90 disk), ventFront (x=0 slots), ventSide (y=65 slots)
#
# fan ON  : fan = prescribed exhaust flow (flowRateInletVelocity, negative),
#           vents = passive pressure inlets (prghTotalPressure).
# fan OFF : fan = blocked (stopped blades ≈ wall), vents stay open →
#           buoyancy-driven natural draft through the slots. This is the
#           physically correct fan-off configuration of the actual CAD
#           (the old model used a pseudo-sealed box instead).
# ---------------------------------------------------------------------------

_TV_WALLS = ("floor", "ceiling", "side_x0", "side_x1", "side_y0", "side_y1")
_TV_VENTS = ("ventFront", "ventSide")


def _tv_ext_wall_T(T_amb_K: float, h: float) -> str:
    return (f"type            externalWallHeatFluxTemperature;\n"
            f"                mode            coefficient;\n"
            f"                Ta              uniform {T_amb_K:.2f};\n"
            f"                h               uniform {h:.4f};\n"
            f"                kappaMethod     fluidThermo;\n"
            f"                value           uniform {T_amb_K:.2f};")


def write_T_tv(p, zero_dir: Path) -> None:
    T_amb_K = p["ambient_C"] + 273.15
    h_floor, h_eff = _compute_h_eff(p)
    fan_on = p.get("fan_rpm", 0) > 1.0

    entries = []
    for w in _TV_WALLS:
        h = h_floor if w == "floor" else h_eff
        entries.append(f"            {w}\n            {{\n"
                       f"                {_tv_ext_wall_T(T_amb_K, h)}\n"
                       f"            }}")
    for v in _TV_VENTS:
        entries.append(f"            {v}\n            {{\n"
                       f"                type            inletOutlet;\n"
                       f"                inletValue      uniform {T_amb_K:.2f};\n"
                       f"                value           uniform {T_amb_K:.2f};\n"
                       f"            }}")
    if fan_on:
        entries.append(f"            fan\n            {{\n"
                       f"                type            inletOutlet;\n"
                       f"                inletValue      uniform {T_amb_K:.2f};\n"
                       f"                value           uniform {T_amb_K:.2f};\n"
                       f"            }}")
    else:
        entries.append(f"            fan\n            {{\n"
                       f"                {_tv_ext_wall_T(T_amb_K, h_eff)}\n"
                       f"            }}")

    body = "\n".join(entries)
    content = _foam_header("volScalarField", "T") + dedent(f"""\
        dimensions      [0 0 0 1 0 0 0];
        internalField   uniform {T_amb_K:.2f};

        boundaryField
        {{
{body}
        }}
        """)
    (zero_dir / "T").write_text(content)


def write_U_tv(p, zero_dir: Path) -> None:
    fan_on = p.get("fan_rpm", 0) > 1.0
    Q = fan_operating_flow(p)

    entries = [f"            {w} {{ type noSlip; }}" for w in _TV_WALLS]
    for v in _TV_VENTS:
        entries.append(f"            {v}\n            {{\n"
                       f"                type            pressureInletOutletVelocity;\n"
                       f"                value           uniform (0 0 0);\n"
                       f"            }}")
    if fan_on:
        # Negative volumetric rate = OUT of the domain (exhaust fan).
        # flowRateInletVelocity enforces the exact Q regardless of the
        # stair-stepped disk area created by cylinderToFace.
        entries.append(f"            fan\n            {{\n"
                       f"                type            flowRateInletVelocity;\n"
                       f"                volumetricFlowRate constant {-Q:.6e};\n"
                       f"                value           uniform (0 0 0);\n"
                       f"            }}")
    else:
        entries.append("            fan { type noSlip; }")

    body = "\n".join(entries)
    content = _foam_header("volVectorField", "U") + dedent(f"""\
        dimensions      [0 1 -1 0 0 0 0];
        internalField   uniform (0 0 0);

        boundaryField
        {{
{body}
        }}
        """)
    (zero_dir / "U").write_text(content)


