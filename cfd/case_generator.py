"""
Parameterized OpenFOAM case generator for the plant module.

Takes a parameter dict, writes a complete buoyantSimpleFoam case
into a target directory.

This is the V1 case: sealed box, single fluid region, heat applied as
fixed heat flux on the floor, all other walls have external convection BC
to ambient. No fan, no vents, no fins yet — those come in V2 once V1
is converging cleanly.

Usage:
    from case_generator import generate_case
    case_dir = generate_case(
        params=dict(
            wall_thickness_mm=2.5,
            wall_k=0.20,
            ambient_C=25.0,
            heat_total_W=17.0,
            mesh_nx=36, mesh_ny=26, mesh_nz=20,
        ),
        target_dir="/tmp/my_case",
    )
"""

from pathlib import Path
from textwrap import dedent


# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

# Plant module dimensions (mm) — fixed by spec
LX_MM, LY_MM, LZ_MM = 90.0, 65.0, 50.0

# ---------------------------------------------------------------------------
# True vent geometry (true_vents=True) — taken straight from the final CAD.
#
# Coordinates are in the INTERNAL cavity frame used by blockMesh:
#   x: 0..90 mm   (flow axis — fan on x=90 short wall)
#   y: 0..65 mm
#   z: 0..50 mm   (floor at z=0)
#
# - Fan: 50 mm 5 V fan (5010 frame) EXHAUSTING through a 45 mm dia opening
#   (the standard vent cut for a 50 mm frame; a full 50 mm hole cannot fit
#   the 50 mm-tall cavity), centred on the x=90 short wall (y=32.5, z=25).
# - Front intake (wall opposite the fan, x=0): 3 slots 50 x 5 mm,
#   horizontally centred (y 7.5..57.5), the slot group centred vertically
#   with 7.5 mm gaps -> slot rows z = 10–15 / 22.5–27.5 / 35–40.
# - Side intake (y=65 outward-facing long wall): 3 slots 40 x 5 mm,
#   centred (x 25..65), same vertical pattern.
# ---------------------------------------------------------------------------

FAN_CENTER_M = (0.090, 0.0325, 0.025)   # on x=90 face
FAN_RADIUS_M = 0.0225                    # 45 mm dia opening for the 50 mm fan

# (y_min, y_max, z_min, z_max) in metres, on the x=0 wall
FRONT_SLOTS_M = [
    (0.0075, 0.0575, 0.0100, 0.0150),
    (0.0075, 0.0575, 0.0225, 0.0275),
    (0.0075, 0.0575, 0.0350, 0.0400),
]

# (x_min, x_max, z_min, z_max) in metres, on the y=65 wall
SIDE_SLOTS_M = [
    (0.025, 0.065, 0.0100, 0.0150),
    (0.025, 0.065, 0.0225, 0.0275),
    (0.025, 0.065, 0.0350, 0.0400),
]

A_FAN_M2   = 3.14159265 * FAN_RADIUS_M**2                       # 1.590e-3
A_VENTS_M2 = (sum((y1-y0)*(z1-z0) for y0, y1, z0, z1 in FRONT_SLOTS_M)
              + sum((x1-x0)*(z1-z0) for x0, x1, z0, z1 in SIDE_SLOTS_M))  # 1.35e-3

# 50 mm 5 V axial fan characteristics (typical 5010 datasheet values,
# Sunon/Delta class), linear fan curve assumed:
#   free flow ~9 CFM and max static pressure ~40 Pa at 6000 rpm,
#   Q_free ∝ rpm, dP_max ∝ rpm² (fan affinity laws).
FAN_FREE_CFM_AT_6000 = 9.0
FAN_DP_MAX_PA_AT_6000 = 40.0
CFM_TO_M3S = 4.719e-4
RHO_AIR = 1.18      # kg/m3 at ~25 C
CD_VENT = 0.62      # sharp-edged slot discharge coefficient


def fan_operating_flow(p) -> float:
    """Fan operating-point volumetric flow (m^3/s) for true_vents mode.

    Intersects a linear fan curve dP = dP_max (1 - Q/Q_free) with the
    system curve dP = K Q^2, where K lumps the intake-slot vena-contracta
    loss and the fan-opening exit dynamic loss:
        K = 0.5 rho [ 1/(Cd A_vents)^2 + 1/A_fan^2 ]
    Solving K Q^2 + (dP_max/Q_free) Q - dP_max = 0 for Q > 0.

    At 6000 rpm this gives ~3.1e-3 m^3/s (~74 % of free flow) — the intake
    slots (1350 mm^2) are smaller than the fan opening (1590 mm^2), so they
    are the dominant restriction.
    """
    rpm = p.get("fan_rpm", 0.0)
    if rpm <= 1.0:
        return 0.0
    s = rpm / 6000.0
    Q_free = FAN_FREE_CFM_AT_6000 * CFM_TO_M3S * s
    dp_max = FAN_DP_MAX_PA_AT_6000 * s * s
    K = 0.5 * RHO_AIR * (1.0 / (CD_VENT * A_VENTS_M2)**2 + 1.0 / A_FAN_M2**2)
    a = dp_max / Q_free
    return (-a + (a * a + 4.0 * K * dp_max)**0.5) / (2.0 * K)


